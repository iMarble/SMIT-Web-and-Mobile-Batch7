// we can declare a variable with 3 types
// 1- let   2- var  3- const

// what is variable?
// variable is just like a container that stores data of multiple types

//      Data types -> integers number, floating number, character, string, boolean, and
//                    list of all above data types -> arrays, object

let num1 = 2;

for (let index = 1; index > 0; index++) {

        if (index != 5) {

            console.log(`${num1} * ${index} = ${num1 * index}`)

        }

}








